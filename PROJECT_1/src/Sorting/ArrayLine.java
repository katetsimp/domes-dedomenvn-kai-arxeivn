package Sorting;
/**
 * it is class that called ArrayLine and contains a String  and an Integer as member variables.
 * 
 * @author katerina 
 *
 */
public class ArrayLine {
	private String word;
	 private int numline;
	 /**
	  * Class constructor
	  * @param word 
	  * @param numline
	  */
	public ArrayLine(String word, int numline) {
		
		super();
		this.word = word;
		this.numline = numline;
	}
	/**
	 * it can get the Word
	 * @return word
	 */
	public String getWord() {
		return word;
	}
	/**
	 * it can change the Word
	 * @param the word before the change.
	 */
	public void setWord(String word) {
		this.word = word;
	}
	/**
	 * it can get the numline
	 * @return numline
	 */
	public int getNumline() {
		return numline;
	}
	/**
	 * it change the  numline.
	 * @param numline before the change.
	 */
	public void setNumline(int numline) {
		this.numline = numline;
	}
	
	
	

	
	
	}
	


